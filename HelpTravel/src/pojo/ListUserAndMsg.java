package pojo;

import java.util.List;

import po.Users;

/**
 * @author Administrator
 *
 */
public class ListUserAndMsg {

	
	private  List<Users> users;
	private int rows;
	private String msg;
	public List<Users> getUsers() {
		return users;
	}
	public void setUsers(List<Users> users) {
		this.users = users;
	}
	
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public int getRows() {
		return rows;
	}
	public void setRows(int rows) {
		this.rows = rows;
	}
	@Override
	public String toString() {
		return "ListUserAndMsg [users=" + users + ", rows=" + rows + ", msg=" + msg + "]";
	}

	
}
